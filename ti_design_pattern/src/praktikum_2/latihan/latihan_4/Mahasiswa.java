package praktikum_2.latihan.latihan_4;

public class Mahasiswa {
    // 1. Atribut private
    private String nama;
    private String nim;

    // 2. Setter untuk nama
    public void setNama(String nama) {
        this.nama = nama;
    }

    // Getter untuk nama
    public String getNama() {
        return nama;
    }

    // Setter untuk nim
    public void setNim(String nim) {
        this.nim = nim;
    }

    // Getter untuk nim
    public String getNim() {
        return nim;
    }
}
